      new StructWrapper<byte>(Pooling.Readonly)
      {
        value = (byte) 254
      },
      new StructWrapper<byte>(Pooling.Readonly)
      {
        value = byte.MaxValue
      }
    };
    public static readonly StructWrapper<bool>[] mappedBoolWrappers =
    [
      new StructWrapper<bool>(Pooling.Readonly) { value = false },
      new StructWrapper<bool>(Pooling.Readonly) { value = true }
    ];
    private readonly Dictionary<Type, StructWrapperPool> pools = new();
    private readonly List<IDisposable> used = new();

    private StructWrapperPool<T> GetPoolForType<T>()
    {
        StructWrapperPool poolForType1;
        if (this.pools.TryGetValue(typeof(T), out poolForType1))
            return poolForType1 as StructWrapperPool<T>;
        StructWrapperPool<T> poolForType2 = new StructWrapperPool<T>(false);
        this.pools.Add(typeof(T), (StructWrapperPool)poolForType2);
        return poolForType2;
    }

    public StructWrapper<byte> Acquire(byte value)
    {
        return StructWrapperPools.mappedByteWrappers[(int)value];
    }

    public StructWrapper<bool> Acquire(bool value)
    {
        return StructWrapperPools.mappedBoolWrappers[value ? 1 : 0];
    }

    public StructWrapper<T> Acquire<T>(T value)
    {
        StructWrapper<T> structWrapper = this.GetPoolForType<T>().Acquire(value);
        this.used.Add((IDisposable)structWrapper);
        return structWrapper;
    }

    public void Clear()
    {
        foreach (IDisposable disposable in this.used)
            disposable.Dispose();
        this.used.Clear();
    }
